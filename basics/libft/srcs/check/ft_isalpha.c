/* ************************************************************************** */
/*                                 ft_isalpha                                 */
/* -------------------------------------------------------------------------- */
/* This function check if c is alphabetic or not                              */
/* Inputs :                                                                   */
/*  - int c : the character to check (refers to the Ascii table)              */
/* Return :                                                                   */
/*  - 1 : if c is alphabetic                                                  */
/*  - 0 : otherwise                                                           */
/* ************************************************************************** */
int	ft_isalpha(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	return (0);
}
