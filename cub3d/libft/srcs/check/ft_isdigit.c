/* ************************************************************************** */
/*                                 ft_isdigit                                 */
/* -------------------------------------------------------------------------- */
/* This function checks if c is a digit (value 0 to 9 included once translate */
/* using the Ascii table)                                                     */
/* Inputs :                                                                   */
/*  - int c : the value to check                                              */
/* Return :                                                                   */
/*  - 1 : if c refers to a digit in the ascii table                           */
/*  - 0 : otherwise                                                           */
/* ************************************************************************** */
int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}
