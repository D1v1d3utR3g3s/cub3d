/* ************************************************************************** */
/*                                 ft_isprint                                 */
/* -------------------------------------------------------------------------- */
/* This function checks if c refers to a printable character in the Ascii     */
/* table)                                                                     */
/* Inputs :                                                                   */
/*  - int c : the value to check                                              */
/* Return :                                                                   */
/*  - 1 : if c refers to a printable character in the ascii table             */
/*  - 0 : otherwise                                                           */
/* ************************************************************************** */
int	ft_isprint(int c)
{
	if (c >= 32 && c < 127)
		return (1);
	return (0);
}
