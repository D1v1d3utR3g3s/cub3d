/* ************************************************************************** */
/*                                 ft_tolower                                 */
/* -------------------------------------------------------------------------- */
/* This function changes an uppercase to a lowercase                          */
/* Inputs :                                                                   */
/*  - int c : the character to modified if needed                             */
/* Return :                                                                   */
/*  - c : the character modified or not                                       */
/* ************************************************************************** */
int	ft_tolower(int c)
{
	if ((c >= 'A') && (c <= 'Z'))
		c = c - 'A' + 'a';
	return (c);
}
