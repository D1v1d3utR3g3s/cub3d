#include "libft.h"

/* ************************************************************************** */
/*                                 ft_strlen                                  */
/* -------------------------------------------------------------------------- */
/* This function returns the length of a string.                              */
/* Inputs :                                                                   */
/*  - const char *s : the string whose size we want                           */
/* Return :                                                                   */
/*  - size_t : the lenght of the string                                       */
/* ************************************************************************** */
size_t	ft_strlen(const char *s)
{
	size_t	len;

	len = 0;
	if (!s)
		return (0);
	while (s[len] != '\0')
		len++;
	return (len);
}
