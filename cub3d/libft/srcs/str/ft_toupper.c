/* ************************************************************************** */
/*                                 ft_toupper                                 */
/* -------------------------------------------------------------------------- */
/* This function changes a lowercase to an uppercase                          */
/* Inputs :                                                                   */
/*  - int c : the character to modified if needed                             */
/* Return :                                                                   */
/*  - c : the character modified or not                                       */
/* ************************************************************************** */
int	ft_toupper(int c)
{
	if ((c >= 'a') && (c <= 'z'))
		c = c - 'a' + 'A';
	return (c);
}
