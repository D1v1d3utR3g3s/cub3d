#include "libft.h"

/* ************************************************************************** */
/*                                 ft_putstr_fd                               */
/* -------------------------------------------------------------------------- */
/* This function writes the string s in fd.                                   */
/* Inputs :                                                                   */
/*  - char *s : the string that will be write in fd                           */
/*  - int  fd : the file descriptor                                           */
/* Return :                                                                   */
/*  - NONE                                                                    */
/* ************************************************************************** */
void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	if (!s)
		return ;
	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar_fd(s[i], fd);
		i++;
	}
}
