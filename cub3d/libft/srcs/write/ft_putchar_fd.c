#include "libft.h"

/* ************************************************************************** */
/*                                 ft_putchar_fd                              */
/* -------------------------------------------------------------------------- */
/* This function writes c in fd.                                              */
/* Inputs :                                                                   */
/*  - char c  : the char that will be write                                   */
/*  - int  fd : the file descriptor                                           */
/* Return :                                                                   */
/*  - NONE                                                                    */
/* ************************************************************************** */
void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}
