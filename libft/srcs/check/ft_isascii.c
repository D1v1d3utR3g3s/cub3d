/* ************************************************************************** */
/*                                 ft_isascii                                 */
/* -------------------------------------------------------------------------- */
/* This function checks if c is in the Ascii table or not                     */
/* Inputs :                                                                   */
/*  - int c : the value to check (can it be translate using the ascii         */
/*   table ?)                                                                 */
/* Return :                                                                   */
/*  - 1 : if c refers to a character in the Ascii table                       */
/*  - 0 : otherwise                                                           */
/* ************************************************************************** */
int	ft_isascii(int c)
{
	if (c >= 0 && c <= 127)
		return (1);
	return (0);
}
