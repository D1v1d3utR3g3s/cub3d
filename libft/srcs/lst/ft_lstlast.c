#include "libft.h"

/* ************************************************************************** */
/*                                 ft_lstlast                                 */
/* -------------------------------------------------------------------------- */
/* This function places the pointer lst to the last element of lst.           */
/* Inputs :                                                                   */
/*  - t_list *lst : a pointer to the beginning of lst                         */
/* Return :                                                                   */
/*  - NONE                                                                    */
/* ************************************************************************** */
t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (lst);
	while (lst->next)
		lst = lst->next;
	return (lst);
}
